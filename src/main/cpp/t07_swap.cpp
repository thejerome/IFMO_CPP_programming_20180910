#include "t07_swap.h"
#include <iostream>


using namespace std;

int t07_swap() {
    int N;
    cin >> N;
    int A[N];
    int temp;
    for (int i=0; i<N; i++){
        cin >> A[i];
    }
    for (int j=0; j<=N-2; j=j+2){
        temp = A[j];
        A[j] = A[j+1];
        A[j+1] = temp;
    }
    for (int k=0; k<N; k++){
        cout << A[k] << " ";
    }
    return 0;

}
